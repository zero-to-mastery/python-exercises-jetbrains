# Strings Exercise
# Complete the string operations

name = 'Python'

# String methods
answer_1 = name.upper()  # Convert to uppercase
answer_2 = name.lower()  # Convert to lowercase
answer_3 = '  hello  '.strip()  # Remove whitespace

# String operations
answer_4 = 'Hello' + ' ' + 'World'  # Concatenate with space
answer_5 = 'Python' * 3  # Repeat 3 times

# String formatting
age = 25
answer_6 = f'I am {age} years old'  # Use f-string with age variable

# String indexing
answer_7 = name[0]  # Get first character
answer_8 = name[-1]  # Get last character
